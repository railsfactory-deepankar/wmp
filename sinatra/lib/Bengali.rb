class Bengalicalendar


 def self.convert(day)
   
   if day == "Monday"
     return "Sombar"
   elsif day == "Tuesday"
     return "mongolbar"
   elsif day == "Wednesday"
     return "budhbar"
   elsif day == "Thursday"
     return "brihospotybar"
   elsif day == "Friday"
     return "shukrabar"
   elsif day == "Saturday"
     return "shonibar"
   else day == "Sunday"
     return "robbar"
  end
 end
end
